public class Licz {
    public static void main(String[] args) {

	  int a = 12;
      int b = 4;
      int c = a + b;
      System.out.println(c+"=12+4");
      c = a - b;
      System.out.println(c+"=12-4");
      c = a * b;
      System.out.println(c+"=12*4");
      c = a / b;
      System.out.println(c+"=12/4");
      c = a % b;
      System.out.println(c+"=12%4");
}
}